-- Equipment table
CREATE TABLE equipment (
    id SERIAL PRIMARY KEY,
    company TEXT NOT NULL,
    role TEXT NOT NULL,
    product TEXT NOT NULL,
    serial TEXT DEFAULT '',
    location TEXT DEFAULT '',
    crate TEXT DEFAULT ''
);

-- Receipts table
CREATE TABLE receipts (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    date TEXT NOT NULL,
    from_unit TEXT NOT NULL,
    to_unit TEXT NOT NULL,
    items JSONB NOT NULL DEFAULT '[]',
    issuer JSONB DEFAULT '{}',
    receiver JSONB DEFAULT '{}',
    notes TEXT DEFAULT '',
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Enable RLS but allow public read for equipment
ALTER TABLE equipment ENABLE ROW LEVEL SECURITY;
ALTER TABLE receipts ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Equipment is viewable by everyone" ON equipment FOR SELECT USING (true);
CREATE POLICY "Receipts are viewable by everyone" ON receipts FOR SELECT USING (true);
CREATE POLICY "Receipts can be inserted by everyone" ON receipts FOR INSERT WITH CHECK (true);

